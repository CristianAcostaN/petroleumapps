    public void textoViscosity2(InputStream is) {
        jTextAreaViscosity2.setText("");
        try {
            BufferedReader read = new BufferedReader(new InputStreamReader(is));
            String linea = read.readLine();
            while (linea != null) {
                jTextAreaViscosity2.append(linea + "\n");
                linea = read.readLine();
            }
        } catch (Exception ex) {
            Logger.getLogger(Porosity.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
